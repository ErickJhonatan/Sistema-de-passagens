    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        if (jComboBox2.getSelectedIndex() == 1) {
            if(user.getMiles() < flight.getPriceInMiles()*Integer.valueOf(jComboBox1.getSelectedItem().toString())){
                javax.swing.JOptionPane.showMessageDialog(null, "Quantidade de milhas insuficiente!", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        Seat[] seats = new Seat[Integer.parseInt(jComboBox1.getSelectedItem().toString())];
        switch (seats.length) {
            case 1:
                if(jTextField1.getText().isBlank() || jFormattedTextField1.getText().isBlank()){
                    javax.swing.JOptionPane.showMessageDialog(null, "Algum campo não foi preenchido!", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (!CPFValidator.isValidCPF(jFormattedTextField1.getText())) {
                    javax.swing.JOptionPane.showMessageDialog(null, "CPF do 1° passageiro é inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                Seat.create(flight.getNumberOfSeats()-flight.getNumberOfSeatsAvailable()+1, jTextField1.getText(), jFormattedTextField1.getText());
                seats[0] = Seat.readHighestId();
                Trip.create(user, flight, seats[0], null, null, jComboBox2.getSelectedIndex() == 1, jCheckBox1.isSelected(), jCheckBox2.isSelected(), jCheckBox3.isSelected());
                break;
            case 2:
                if(jTextField1.getText().isBlank() || jFormattedTextField1.getText().isBlank() || jTextField2.getText().isBlank() || jFormattedTextField2.getText().isBlank()){
                    javax.swing.JOptionPane.showMessageDialog(null, "Algum campo não foi preenchido!", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (!CPFValidator.isValidCPF(jFormattedTextField1.getText())) {
                    javax.swing.JOptionPane.showMessageDialog(null, "CPF do 1° passageiro é inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (!CPFValidator.isValidCPF(jFormattedTextField2.getText())) {
                    javax.swing.JOptionPane.showMessageDialog(null, "CPF do 2° passageiro é inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                Seat.create(flight.getNumberOfSeats()-flight.getNumberOfSeatsAvailable()+1, jTextField1.getText(), jFormattedTextField1.getText());
                seats[0] = Seat.readHighestId();
                Seat.create(flight.getNumberOfSeats()-flight.getNumberOfSeatsAvailable()+2, jTextField2.getText(), jFormattedTextField2.getText());
                seats[1] = Seat.readHighestId();
                Trip.create(user, flight, seats[0], seats[1], null, jComboBox2.getSelectedIndex() == 1, jCheckBox1.isSelected(), jCheckBox2.isSelected(), jCheckBox3.isSelected());
                break;
            case 3:
                if(jTextField1.getText().isBlank() || jFormattedTextField1.getText().isBlank() || jTextField2.getText().isBlank() || jFormattedTextField2.getText().isBlank()
                        || jTextField3.getText().isBlank() || jFormattedTextField3.getText().isBlank()){
                    javax.swing.JOptionPane.showMessageDialog(null, "Algum campo não foi preenchido!", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (!CPFValidator.isValidCPF(jFormattedTextField1.getText())) {
                    javax.swing.JOptionPane.showMessageDialog(null, "CPF do 1° passageiro é inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (!CPFValidator.isValidCPF(jFormattedTextField2.getText())) {
                    javax.swing.JOptionPane.showMessageDialog(null, "CPF do 2° passageiro é inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (!CPFValidator.isValidCPF(jFormattedTextField3.getText())) {
                    javax.swing.JOptionPane.showMessageDialog(null, "CPF do 3° passageiro é inválido!", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                Seat.create(flight.getNumberOfSeats()-flight.getNumberOfSeatsAvailable()+1, jTextField1.getText(), jFormattedTextField1.getText());
                seats[0] = Seat.readHighestId();
                Seat.create(flight.getNumberOfSeats()-flight.getNumberOfSeatsAvailable()+2, jTextField2.getText(), jFormattedTextField2.getText());
                seats[1] = Seat.readHighestId();
                Seat.create(flight.getNumberOfSeats()-flight.getNumberOfSeatsAvailable()+3, jTextField3.getText(), jFormattedTextField3.getText());
                seats[2] = Seat.readHighestId();
                Trip.create(user, flight, seats[0], seats[1], seats[2], jComboBox2.getSelectedIndex() == 1, jCheckBox1.isSelected(), jCheckBox2.isSelected(), jCheckBox3.isSelected());
                break;
        }
        Flight.update(flight.getId(), flight.getOriginCity(), flight.getDestinationCity(), 
                flight.getDepartureDate(), flight.getReturnDate(), flight.getPrice(), flight.getNumberOfSeats(), 
                flight.getNumberOfSeatsAvailable()-seats.length);
        String response = "Compra realizada com sucesso!\n";
        if (jComboBox2.getSelectedIndex() == 1) {
            User.update(user.getId(), user.getName(), user.getCpf(), user.getMiles()-price, user.getUsername(), user.getPassword());
            
            response += "Você comprou usando milhas,\n";
            response += "seu saldo agora é de: " + (user.getMiles()-price);
        }
        javax.swing.JOptionPane.showMessageDialog(null, response, "Mensagem", JOptionPane.INFORMATION_MESSAGE);
        this.setVisible(false);
    }//GEN-LAST:event_jButton7ActionPerformed


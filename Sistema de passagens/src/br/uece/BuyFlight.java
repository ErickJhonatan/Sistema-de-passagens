    private void updatePrice(){
        price = 0f;
        if(jComboBox2.getSelectedIndex() == 0){
            price = flight.getPrice();
        }else{
            price = flight.getPriceInMiles();
        }
        price *= Integer.valueOf(jComboBox1.getSelectedItem().toString());
        if(jCheckBox1.isSelected())
            if(jComboBox2.getSelectedIndex() == 0)
                price += 10;
            else
                price += 10*100;
        if(jCheckBox2.isSelected())
            if(jComboBox2.getSelectedIndex() == 0)
                price += 10;
            else
                price += 10*100;
        if(jCheckBox3.isSelected())
            if(jComboBox2.getSelectedIndex() == 0)
                price += 10;
            else
                price += 10*100;
        if(jComboBox2.getSelectedIndex() == 0){
            jLabel18.setText("R$" + price);
        }else{
            jLabel18.setText(price+" milhas");
        }
    }
    

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
    if(evt.getClickCount() == 2){
            int row = jTable2.getSelectedRow();
            Object value = jTable2.getValueAt(row, 0);
            Integer id = Integer.valueOf(value.toString());
            Trip trip = Trip.readById(id);
            System.out.println(trip.getId());
            if(!trip.getCheckIn()){
                int result = JOptionPane.showConfirmDialog(this, "Deseja fazer o check-in e ganhar suas milhas?", "Realizar check-in", JOptionPane.YES_NO_OPTION);
                if (result == JOptionPane.YES_OPTION) {
                    Integer numberOfSeats = 1;
                    if(trip.getSeatTwo() != null)
                        numberOfSeats++;
                    if(trip.getSeatThree()!= null)
                        numberOfSeats++;
                    Float miles = trip.getFlight().getPrice()*numberOfSeats;
                    String message = "Check-in realizado com sucesso!\n";
                    
                    if(!trip.getBoughtWithMiles())
                        message += "Você ganhou " + miles + " milhas.\n";
                    else
                        miles = 0f;
                    message += "Seus assentos são: " + trip.getSeatOne().getNumber();
                    if(trip.getSeatTwo()!=null)
                        message += ", " + trip.getSeatTwo().getNumber();
                    if(trip.getSeatThree()!=null)
                        message += ", " + trip.getSeatThree().getNumber();
                    JOptionPane.showMessageDialog(this, message);
                    
                    User.update(actualUser.getId(), actualUser.getName(), actualUser.getCpf(), actualUser.getMiles()+miles, actualUser.getUsername(), actualUser.getPassword());
                    Trip.update(trip.getId(), trip.getUser(), trip.getFlight(), trip.getSeatOne(), trip.getSeatTwo(), trip.getSeatThree(), true, trip.getBoughtWithMiles(), trip.getBoughtLuggage(), trip.getBoughtHotel(), trip.getBoughtCar());
                    updateMyFlights();
                    updateActualUser();
                }
            }else{
                String message = "Seus assentos são: " + trip.getSeatOne().getNumber();
                if(trip.getSeatTwo()!=null)
                    message += ", " + trip.getSeatTwo().getNumber();
                if(trip.getSeatThree()!=null)
                    message += ", " + trip.getSeatThree().getNumber();
                JOptionPane.showMessageDialog(this, message);
            }
            return;
        }
    }//GEN-LAST:event_jTable2MouseClicked


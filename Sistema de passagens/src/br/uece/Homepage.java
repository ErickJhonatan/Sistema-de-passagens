    private void updateFlights() {
        DefaultTableModel new_model = new DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "#", "Cidade de Origem", "Cidade de Destino", "Data da Ida", "Data da Volta", "Preço", "Preço em Milhas", "Qnt. de Assentos", "Qnt. de Assentos Livres"
                }
        ) {
            boolean[] canEdit = new boolean[]{
                false, false, false, false, false, false, false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        };
        Map<String, String> filters = new HashMap<String, String>();
        if(!jTextField10.getText().isBlank()){
            filters.put("origin_city", jTextField10.getText());
        }
        if(!jTextField13.getText().isBlank()){
            filters.put("destination_city", jTextField13.getText());
        }
        ArrayList<Flight> flights = Flight.read(actualUser.getIsAdmin(), filters);
        for (Flight flight : flights) {
            new_model.addRow(new Object[]{flight.getId(), flight.getOriginCity(), flight.getDestinationCity(), flight.getDepartureDate(), flight.getReturnDate(), "R$" + flight.getPrice(), flight.getPriceInMiles(), flight.getNumberOfSeats(), flight.getNumberOfSeatsAvailable()});
        }
        jTable1.setModel(new_model);
    }
    

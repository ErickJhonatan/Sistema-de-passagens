    private void jTable3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3MouseClicked
        if (evt.getClickCount() == 2 && actualUser.getIsAdmin()) {
            int row = jTable3.getSelectedRow();
            Object value = jTable3.getValueAt(row, 0);
            Flight flight = Flight.readById(Integer.valueOf(value.toString()));
            String message = "Assentos comprados: \n";
            Integer numberOfSeatsBought = flight.getNumberOfSeats()-flight.getNumberOfSeatsAvailable();
            for (int i = 1; i <= numberOfSeatsBought; i++) {
                message += i;
                if(i != numberOfSeatsBought)
                    message += ", ";
                if(i % 10 == 0)
                    message += "\n";
            }
            JOptionPane.showMessageDialog(this, message);
        }
    }//GEN-LAST:event_jTable3MouseClicked

